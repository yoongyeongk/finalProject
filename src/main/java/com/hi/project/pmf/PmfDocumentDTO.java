package com.hi.project.pmf;

public class PmfDocumentDTO {

	private int docu_num;
	private int num;
	private String contents;
	
	public int getDocu_num() {
		return docu_num;
	}
	public void setDocu_num(int docu_num) {
		this.docu_num = docu_num;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getContents() {
		return contents;
	}
	public void setContents(String contents) {
		this.contents = contents;
	}
}
